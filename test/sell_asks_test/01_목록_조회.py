"""
내 판매입찰 목록 조회 — 안전 (읽기만).

설정: 아래 상수만 수정하면 됨.
"""

import _common
_common.init_session()

# ─────────────────────────────────────────────────────────────
# 설정 (필요 시 수정)
# ─────────────────────────────────────────────────────────────
상태_필터 = "입찰중"   # None / "입찰중" / "만료"
최대_출력 = 20         # 콘솔에 찍을 최대 건수 (너무 많으면 앞에서 잘라봄)
# ─────────────────────────────────────────────────────────────

from markets.kream import sell_asks  # noqa: E402


def main():
    print(f"\n조회 시작 (상태_필터={상태_필터})")
    목록 = sell_asks.내_판매입찰_목록(상태_필터=상태_필터)
    print(f"총 {len(목록)}건\n")

    for i, d in enumerate(목록[:최대_출력], start=1):
        print(
            f"[{i}] id={d['id']}  "
            f"product_id={d['product_id']}  option={d['option']}  "
            f"price={d['price']:,}  정산={d['정산금액']:,}  "
            f"상태={d['상태']}  만료={d['만료일']}"
        )
        print(f"     {d['상품명_국문']} (품번={d['품번']})")

    if len(목록) > 최대_출력:
        print(f"\n... 이하 {len(목록) - 최대_출력}건 생략")


if __name__ == "__main__":
    main()
