"""
core/pricing.py 검증 — 순수 함수, 네트워크 호출 없음.

각 케이스는 (설명, 경쟁_최저가, 공급가, 감산, 기대값) 튜플.
감산이 None 이면 기본값 사용.
"""

import _common  # noqa: F401

from core.pricing import 제안_입찰가, DEFAULT_UNDERCUT


def 구분선(제목):
    print()
    print("=" * 60)
    print(제목)
    print("=" * 60)


# ─────────────────────────────────────────────────────────────
# 케이스 정의
# ─────────────────────────────────────────────────────────────
CASES = [
    # (설명,                                경쟁,     공급가,  감산,    기대값)
    ("경쟁 여유: 경쟁-1000 이 공급가보다 위", 130_000, 120_000, None, 129_000),
    ("경쟁 타이트: 경쟁-1000 이 공급가보다 아래",
                                             120_500, 120_000, None, 120_000),
    ("경쟁=공급가+1000 경계",                 121_000, 120_000, None, 120_000),
    ("경쟁=공급가 경계",                       120_000, 120_000, None, 120_000),
    ("경쟁 < 공급가",                          115_000, 120_000, None, 120_000),
    ("경쟁자 없음 (None) → 공급가",            None,    120_000, None, 120_000),
    ("공급가 None → None",                    130_000, None,    None, None),
    ("공급가 0 → None",                       130_000, 0,       None, None),
    ("공급가 음수 → None",                    130_000, -1,      None, None),
    ("천원 단위 내림: 경쟁 130,500",          130_500, 100_000, None, 129_000),
    ("공급가가 천원 단위 아님: 120,500 → 120,000",
                                             130_000, 120_500, None, 129_000),
    ("공급가 천원 단위 아님 + 경쟁 여유 적음",   121_500, 120_500, None, 120_000),
    ("감산 0 (낮추지 않음): 경쟁가 그대로",    130_000, 100_000, 0,    130_000),
    ("감산 5000",                             130_000, 100_000, 5_000, 125_000),
    ("감산 음수 → 0 으로 방어",                130_000, 100_000, -500, 130_000),
    ("공급가 천원 미만 (500) → None (단위 미만은 못 올림)",
                                             999,     500,     None, None),
]


def case(설명, 경쟁, 공급, 감산, 기대):
    kwargs = {}
    if 감산 is not None:
        kwargs["감산"] = 감산
    실제 = 제안_입찰가(경쟁, 공급, **kwargs)
    ok = 실제 == 기대
    mark = "✅" if ok else "❌"
    print(f"  {mark} {설명:45s}"
          f" 경쟁={경쟁}  공급={공급}  감산={감산 if 감산 is not None else DEFAULT_UNDERCUT}"
          f" → 실제={실제}  기대={기대}")
    return ok


def main():
    구분선("core/pricing.제안_입찰가 — 경계값 테스트")
    results = [case(*c) for c in CASES]

    구분선(f"총 {len(results)}건 중 {sum(results)}건 통과")
    if all(results):
        print("[OK] 전체 통과")
    else:
        실패 = len(results) - sum(results)
        print(f"[FAIL] {실패}건 실패")


if __name__ == "__main__":
    main()
